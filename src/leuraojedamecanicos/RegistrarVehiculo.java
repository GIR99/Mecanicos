    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        
        if(EntradaIdVehiculo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No dejes el campo vacio. Introduce un valor...", "ERROR", JOptionPane.WARNING_MESSAGE);
        }else{
        ConexionMySQL con=new ConexionMySQL();
        con.ConectarBasedeDatos();
        String sql="select idClienteV,Vehiculo,Serie,Placa from vehiculos where idVehiculos="+"'"+EntradaIdVehiculo.getText()+"'";
        String Usuarios[]=new String[4];
        Statement set;
        try {
            set=con.conexion.createStatement();
            con.resultado=set.executeQuery(sql);
            while(con.resultado.next())
            {
                //Se guarda en el arreglo y del otro lado es la columna del valor
                Usuarios[0]=con.resultado.getString(1);
                Usuarios[1]=con.resultado.getString(2);
                Usuarios[2]=con.resultado.getString(3);
                Usuarios[3]=con.resultado.getString(4);
            }
            EntradaIdCliente.setText(Usuarios[0]);
            EntradaVehiculo.setText(Usuarios[1]);
            EntradaSerie.setText(Usuarios[2]);
            EntradaPlaca.setText(Usuarios[3]);
            
        } catch (SQLException ex) {
            Logger.getLogger(Registros.class.getName()).log(Level.SEVERE, null, ex);
        } 
       }
    }//GEN-LAST:event_jButton8ActionPerformed

