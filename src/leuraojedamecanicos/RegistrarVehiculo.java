    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         ConexionMySQL Con=new ConexionMySQL();
        if(EntradaIdCliente.getText().isEmpty()||EntradaVehiculo.getText().isEmpty()||EntradaSerie.getText().isEmpty()||EntradaPlaca.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Procure no dejar espacios vacios", "ERROR", JOptionPane.WARNING_MESSAGE);
        }else{
        try{
            String u= EntradaIdCliente.getText();
            String c= EntradaPlaca.getText();
            Con.ConectarBasedeDatos();
            String SQL="SELECT COUNT(IdVehiculos) AS i FROM Vehiculos WHERE IdClienteV='"+u+"'"+" AND Placa= '"+c+"'";
            Con.resultado=Con.sentencia.executeQuery(SQL);
            while (Con.resultado.next()){
                if (Con.resultado.getString("i").equals("1")){
                    JOptionPane.showMessageDialog(null, "El Vehiculo ya se encuentra Registrado", "Error al registrar", 0);
                    EntradaIdCliente.setText("");
                    EntradaVehiculo.setText("");
                    EntradaSerie.setText("");
                    EntradaPlaca.setText("");
                }else{
        String ntec=EntradaIdCliente.getText();
        int idec=Integer.parseInt(ntec);
        Con.ConectarBasedeDatos();
        Con.GuardarVehiculo(idec, EntradaVehiculo.getText(), EntradaSerie.getText(), EntradaPlaca.getText());
        EntradaIdCliente.setText("");
        EntradaVehiculo.setText("");
        EntradaSerie.setText("");
        EntradaPlaca.setText("");
        Mostrar("");

                }
            }
            Con.DesconectarBasedeDatos();
        } catch (SQLException ex){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE,null,ex);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

